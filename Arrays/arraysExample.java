package Arrays;

import java.util.*;

public class arraysExample {
    public static void main(String args[]) {
        int[] marks = new int[3];
        marks[0] = 90;
        marks[1] = 85;
        marks[2] = 70;

        for (int i=0; i<3;i++) {
            System.out.println(marks[i]);
        }
    }
    
}
